#include<bits/stdc++.h>
using namespace std;
typedef long long int lli;
int main() {
    int t;
    cin >> t;

    while (t--)
    {
      int n;
      cin >> n;
      vector<lli>arr(n);
      lli sum = 0;
      for(lli i=0; i<n; i++)
      {
          cin >> arr[i];
          sum += abs(arr[i]);
      }
      cout << sum << endl;
    }

    return 0;
}
